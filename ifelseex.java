public class ifelseex {
    public static void main(String[] args) {
        int a = 18;
        int b = 17;

        // a > b
        // a < b
        // a = b

        // condition -> true/false
        if(a > b) {
            System.out.println(a + " is bigger than " + b);
        } else if(a < b) {
            System.out.println(b + " is bigger than " + a);
        } else {
            System.out.println("both are equal");
        }
    }
}
